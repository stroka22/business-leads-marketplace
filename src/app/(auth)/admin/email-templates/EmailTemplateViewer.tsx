const templates = [
  {
    id: 'promotional',
    name: 'Main Promotional Email',
    description: 'Primary promotional email showcasing all financing options and the qualification process.',
    subject: 'Get Pre-Qualified for Business Financing in 60 Seconds',
    preheader: 'Equipment loans, SBA financing, working capital & more. No credit impact.',
    html: promotionalEmailHTML,
  },
]

export default function EmailTemplateViewer() {
  const [selectedTemplate, setSelectedTemplate] = useState(templates[0])
  const [copied, setCopied] = useState(false)
  const [viewMode, setViewMode] = useState<'preview' | 'code'>('preview')

  const copyToClipboard = () => {
    navigator.clipboard.writeText(selectedTemplate.html)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  const downloadHTML = () => {
    const blob = new Blob([selectedTemplate.html], { type: 'text/html' })
    const url = URL.createObjectURL(blob)
    const a = document.createElement('a')
    a.href = url
    a.download = selectedTemplate.id + '-email-template.html'
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
  }

  return (
    <div className="grid lg:grid-cols-4 gap-6">
      <div className="lg:col-span-1">
        <div className="bg-white rounded-xl shadow-sm border border-gray-200 p-4">
          <h3 className="font-semibold text-gray-900 mb-4">Templates</h3>
          <div className="space-y-2">
            {templates.map((template) => (
              <button
                key={template.id}
                onClick={() => setSelectedTemplate(template)}
                className={`w-full text-left p-3 rounded-lg transition-colors ${
                  selectedTemplate.id === template.id
                    ? 'bg-emerald-50 border-2 border-emerald-500'
                    : 'bg-gray-50 border-2 border-transparent hover:bg-gray-100'
                }`}
              >
                <p className="font-medium text-gray-900 text-sm">{template.name}</p>
                <p className="text-xs text-gray-500 mt-1">{template.description}</p>
              </button>
            ))}
          </div>
        </div>

        <div className="bg-white rounded-xl shadow-sm border border-gray-200 p-4 mt-4">
          <h3 className="font-semibold text-gray-900 mb-3">Email Details</h3>
          <div className="space-y-3 text-sm">
            <div>
              <p className="text-gray-500 text-xs uppercase tracking-wide">Subject Line</p>
              <p className="text-gray-900 font-medium">{selectedTemplate.subject}</p>
            </div>
            <div>
              <p className="text-gray-500 text-xs uppercase tracking-wide">Preheader</p>
              <p className="text-gray-700">{selectedTemplate.preheader}</p>
            </div>
          </div>
        </div>

        <div className="bg-white rounded-xl shadow-sm border border-gray-200 p-4 mt-4">
          <h3 className="font-semibold text-gray-900 mb-3">Actions</h3>
          <div className="space-y-2">
            <button
              onClick={copyToClipboard}
              className="w-full px-4 py-2 bg-emerald-600 text-white rounded-lg font-medium hover:bg-emerald-700 transition-colors flex items-center justify-center gap-2"
            >
              {copied ? (
                <>
                  <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
                  </svg>
                  Copied!
                </>
              ) : (
                <>
                  <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 16H6a2 2 0 01-2-2V6a2 2 0 012-2h8a2 2 0 012 2v2m-6 12h8a2 2 0 002-2v-8a2 2 0 00-2-2h-8a2 2 0 00-2 2v8a2 2 0 002 2z" />
                  </svg>
                  Copy HTML
                </>
              )}
            </button>
            <button
              onClick={downloadHTML}
              className="w-full px-4 py-2 bg-gray-100 text-gray-700 rounded-lg font-medium hover:bg-gray-200 transition-colors flex items-center justify-center gap-2"
            >
              <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
              </svg>
              Download HTML
            </button>
          </div>
        </div>
      </div>

      <div className="lg:col-span-3">
        <div className="bg-white rounded-xl shadow-sm border border-gray-200 mb-4">
          <div className="flex border-b border-gray-200">
            <button
              onClick={() => setViewMode('preview')}
              className={`flex-1 px-4 py-3 text-sm font-medium transition-colors ${
                viewMode === 'preview'
                  ? 'text-emerald-600 border-b-2 border-emerald-600 bg-emerald-50/50'
                  : 'text-gray-500 hover:text-gray-700'
              }`}
            >
              Preview
            </button>
            <button
              onClick={() => setViewMode('code')}
              className={`flex-1 px-4 py-3 text-sm font-medium transition-colors ${
                viewMode === 'code'
                  ? 'text-emerald-600 border-b-2 border-emerald-600 bg-emerald-50/50'
                  : 'text-gray-500 hover:text-gray-700'
              }`}
            >
              HTML Code
            </button>
          </div>
        </div>

        <div className="bg-white rounded-xl shadow-sm border border-gray-200 overflow-hidden">
          {viewMode === 'preview' ? (
            <div className="bg-gray-100 p-4">
              <iframe
                srcDoc={selectedTemplate.html}
                title="Email Preview"
                className="w-full bg-white rounded-lg shadow-sm"
                style={{ height: '800px', border: 'none' }}
              />
            </div>
          ) : (
            <div className="p-4">
              <pre className="bg-gray-900 text-gray-100 p-4 rounded-lg overflow-x-auto text-xs leading-relaxed" style={{ maxHeight: '800px' }}>
                <code>{selectedTemplate.html}</code>
              </pre>
            </div>
          )}
        </div>
      </div>
    </div>
  )
}
